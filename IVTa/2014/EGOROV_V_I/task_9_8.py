'''
Создайте игру, в которой компьютер выбирает
какое-либо слово, а игрок должен его отгадать.
Компьютер сообщает игроку, сколько букв в слове,
и дает пять попыток узнать, есть ли какая-либо
буква в слове, причем программа может отвечать
только "Да" и "Нет". Вслед за тем игрок должен
попробовать отгадать слово.
'''
